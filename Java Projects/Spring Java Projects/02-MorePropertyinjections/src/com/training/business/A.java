package com.training.business;

public class A {

	int num;

	public A(int num) {
		super();
		this.num = num;
	}
	

	public int getNum() {
		return num;
	}


	public void setNum(int num) {
		this.num = num;
	}


	@Override
	public String toString() {
		return "A [num=" + num + "]";
	}

	public A() {
		super();
	}
	
	
	
	
}
